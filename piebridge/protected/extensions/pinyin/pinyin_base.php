	public static function get($str)
	{
		$max = strlen($str);
		$py = $result = "";
		for($i=0; $i<$max; $i++)
		{
			$h = ord($str[$i]);
			if($h>=0x81 && $h<=0xfe && $i+1 < $max)
			{
				$l=ord($str[$i+1]);
				if($h==161 && $l==64){ $py.="_";}
				elseif($l>=0x40 && $l<=0xfe && $l!=0x7f)
				{
					$match_pattern='|'.preg_quote(chr($h).chr($l))."([a-z0-9'`~\!@#\$%\^&\*\(\)-\=_\+\{\}\|\[\]\\;'\:\",\./\<\>\?\ ]+)|";
					preg_match($match_pattern, self::$py_table, $m);
					$py .= (count($m)>1) ? $m[1] : chr($h).chr($l);
					$i++;
				}
			}
			else{ $py.=chr($h); }
		}
		return $py;
	}
